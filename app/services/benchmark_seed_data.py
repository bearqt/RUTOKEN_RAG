from __future__ import annotations


def page_url(page_id: int) -> str:
    return f"https://dev.rutoken.ru/pages/viewpage.action?pageId={page_id}"


def question(
    case_key: str,
    prompt: str,
    reference_answer: str,
    *,
    page_id: int,
    tags: tuple[str, ...],
    required_terms: tuple[str, ...] = (),
    required_any: tuple[tuple[str, ...], ...] = (),
    forbidden_terms: tuple[str, ...] = (),
    exact_answer: str | None = None,
    notes: str | None = None,
) -> dict:
    return {
        "case_key": case_key,
        "question": prompt,
        "tags": list(tags),
        "reference_answer": reference_answer,
        "exact_answer": exact_answer,
        "required_terms": list(required_terms),
        "required_any": [list(group) for group in required_any],
        "forbidden_terms": list(forbidden_terms),
        "required_sources": [page_url(page_id)],
        "expected_refusal": False,
        "notes": notes,
    }


def routing_case(
    case_key: str,
    prompt: str,
    *,
    title: str,
    page_id: int,
    tags: tuple[str, ...],
    required_terms: tuple[str, ...],
    notes: str | None = None,
) -> dict:
    return question(
        case_key,
        prompt,
        f"Это описано в инструкции «{title}».",
        page_id=page_id,
        tags=tags + ("routing",),
        required_terms=required_terms,
        notes=notes,
    )


